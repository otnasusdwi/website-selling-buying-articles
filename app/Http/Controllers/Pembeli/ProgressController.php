        $orders = Order::with(['user', 'tipe_artikel', 'harga'])
        ->where('pembeli_id', Auth::user()->id)
        ->where('status', 'paid')
        ->whereNotIn('status_order', ['pending'])
        ->orderBy('tanggal_ambil', 'DESC')
        ->paginate(10);
